// var
// var <variableName>; 

var number; // DECLARING A VARIABLE. ==> value is undefined.
number = 10; // puts the value 10 in variable called as number. ASSIGNMENT

var primeNumber = 7; // DECLARING + ASSIGNING.
primeNumber = 11; // RE-ASSIGNING. value varies/changes.
